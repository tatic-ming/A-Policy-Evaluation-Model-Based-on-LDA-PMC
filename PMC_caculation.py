def load_word_list(path):
    with open(path, 'r', encoding='utf-8') as f:
        return list(set(line.strip() for line in f if line.strip()))


custom_terms = load_word_list(r"E:\python\pythonProject\dict.txt")
custom_stopwords = load_word_list(r"E:\python\pythonProject\stop_words.txt")

patterns = [nlp_zh(term) for term in custom_terms]
matcher.add("CUSTOM_TERMS", None, *patterns)


def preprocess_text_zh(text):
    doc = nlp_zh(text)
    matches = matcher(doc)
    custom_spans = []
    for _, start, end in matches:
        span = Span(doc, start, end, label="CUSTOM_TERM")
        custom_spans.append(span)
    all_spans = list(doc.ents) + custom_spans
    filtered_spans = filter_spans(all_spans)
    doc.ents = filtered_spans

    tokens = []
    current_phrase = []
    for token in doc:
        if token.text in custom_stopwords or token.is_punct:
            continue
        if token.ent_type_ == "CUSTOM_TERM":
            if current_phrase:
                tokens.append("".join(current_phrase))
                current_phrase = []
            tokens.append(token.text)
            continue
        if token.pos_ in ['NOUN', 'VERB', 'PROPN']:
            current_phrase.append(token.text)
        else:
            if current_phrase:
                tokens.append("".join(current_phrase))
                current_phrase = []
    if current_phrase:
        tokens.append("".join(current_phrase))
    return set(tokens)


# ================== 核心计算模块（与历史版本完全一致） ==================
class NationalQuantileCalculator:
    def __init__(self):
        self.quantile_map = defaultdict(dict)
        self.pmc_quantile_map = None

    def build_national_pool(self):
        print("正在构建全国分位数基准池...")
        raw_metrics = defaultdict(list)
        pmc_scores = []

        for province, path in PROVINCE_PATHS.items():
            if not os.path.exists(path):
                continue
            files = [f for f in os.listdir(path) if f.endswith(('.txt', '.md'))]
            for file in files:
                file_path = os.path.join(path, file)
                try:
                    with open(file_path, 'r', encoding='utf-8') as f:
                        text = f.read()
                        scores = self._calculate_raw_scores(text)
                        for dim in PMC_KEYWORDS.keys():
                            raw_metrics[dim].append(scores.get(dim, 0))
                        pmc_scores.append(sum(scores.values()))
                except Exception as e:
                    print(f"文件处理失败 {file_path}: {str(e)}")

        print("正在计算指标分位数...")
        for dim in PMC_KEYWORDS.keys():
            self._calculate_dim_quantiles(dim, raw_metrics[dim])

        print("正在计算PMC分位数...")
        self._calculate_pmc_quantiles(pmc_scores)

    def _calculate_raw_scores(self, text):  # 与历史版本完全一致的原始分计算逻辑
        scores = {}
        for dim, config in PMC_KEYWORDS.items():
            sub_dims = config["sub_dimensions"]
            total_sub = len(sub_dims)
            matched_sub = 0
            for sub, keywords in sub_dims.items():
                pattern = r'(' + '|'.join(map(re.escape, keywords)) + r')'
                if re.search(pattern, text):
                    matched_sub += 1
            scores[dim] = matched_sub / total_sub if total_sub > 0 else 0
        return scores

    def _calculate_dim_quantiles(self, dim, scores):  # 与历史版本完全一致的分位数映射
        sorted_scores = np.sort(scores)
        self.quantile_map[dim] = {
            'values': sorted_scores,
            'quantiles': np.linspace(0, 1, len(sorted_scores))
        }

    def _calculate_pmc_quantiles(self, scores):  # 与历史版本完全一致的PMC分位数映射
        sorted_scores = np.sort(scores)
        self.pmc_quantile_map = {
            'values': sorted_scores,
            'quantiles': np.linspace(0, 1, len(sorted_scores))
        }

    def get_quantile(self, dim, score):  # 与历史版本完全一致的分位数查询
        if dim == 'PMC':
            quant_info = self.pmc_quantile_map
        else:
            quant_info = self.quantile_map.get(dim)

        if not quant_info:
            return 0.5

        idx = np.searchsorted(quant_info['values'], score, side='right')
        idx = min(idx, len(quant_info['quantiles']) - 1)
        return quant_info['quantiles'][idx]


class ProvinceProcessor:
    def __init__(self, province_name, province_path, quantile_calculator):  # 参数名与属性名严格一致
        self.province_name = province_name
        self.path = province_path
        self.quantile_calculator = quantile_calculator  # 属性名与错误提示完全一致
        self.policy_data = []  # 存储单个省份所有政策文件的详细评估结果
        self.pmc_scores = []  # 存储单个省份所有政策的PMC分位数值（用于KDE计算）
        self.dim_scores = defaultdict(list)  # 存储各一级指标分位数值（用于KDE计算）

    def process_province(self):  # 与历史版本一致的政策文件处理逻辑
        if not os.path.exists(self.path):
            return self

        files = [f for f in os.listdir(self.path) if f.endswith(('.txt', '.md'))]
        for file in files:
            file_path = os.path.join(self.path, file)
            try:
                with open(file_path, 'r', encoding='utf-8') as f:
                    text = f.read()
                    raw_scores = self.quantile_calculator._calculate_raw_scores(text)
                    quant_scores = {
                        dim: self.quantile_calculator.get_quantile(dim, score)
                        for dim, score in raw_scores.items()
                    }
                    quant_pmc = self.quantile_calculator.get_quantile('PMC', sum(raw_scores.values()))

                    # 收集用于KDE计算的原始数据（与历史版本一致）
                    self.pmc_scores.append(quant_pmc)
                    for dim in PMC_KEYWORDS.keys():
                        self.dim_scores[dim].append(quant_scores[dim])

                    # 构建详细记录（与历史版本完全一致的字段结构）
                    record = {
                        '文件': file,
                        '原始PMC': sum(raw_scores.values()),
                        '分位数PMC': quant_pmc
                    }
                    for dim in PMC_KEYWORDS.keys():
                        record[f"{dim}_原始"] = raw_scores[dim]
                        record[f"{dim}_分位数"] = quant_scores[dim]
                    self.policy_data.append(record)
            except Exception as e:
                print(f"文件处理失败 {file_path}: {str(e)}")
        return self

    def aggregate_data(self):  # 与历史版本一致的聚合逻辑
        aggregation = {
            'PMC': self._aggregate_pmc(),
            **{dim: self._aggregate_dimension(dim) for dim in PMC_KEYWORDS.keys()}
        }
        return aggregation

    def _aggregate_pmc(self):  # 与历史版本一致的PMC聚合逻辑
        return self._apply_aggregation(self.pmc_scores, 'PMC')

    def _aggregate_dimension(self, dim):  # 与历史版本一致的一级指标聚合逻辑
        return self._apply_aggregation(self.dim_scores[dim], dim)

    def _apply_aggregation(self, scores, dim_name):  # 与历史版本一致的聚合方法
        if not scores:
            return 0.0

        try:
            if AGG_METHOD == 'median':
                return round(float(np.median(scores)), 4)
            elif AGG_METHOD == 'top_ratio':
                return round(float(np.mean(np.array(scores) >= 0.6)), 4)
            elif AGG_METHOD == 'kde_peak':  # 与历史版本一致的KDE峰值计算
                kde = gaussian_kde(scores)
                x = np.linspace(0, 1, 100)
                peak_x = x[np.argmax(kde(x))]
                return round(float(peak_x), 4)
            else:
                return round(float(np.median(scores)), 4)
        except Exception as e:
            print(f"聚合计算失败（{self.province_name}-{dim_name}）: {str(e)}")
            return round(float(np.median(scores)), 4)


